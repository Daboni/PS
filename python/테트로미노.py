blocks = (
    ((0,1), (0,2), (0,3)),
    ((1,0), (2,0), (3,0)),
    ((1,0), (1,1), (1,2)),
    ((0,1), (1,0), (2,0)),
    ((0,1), (0,2), (1,2)),
    ((1,0), (2,0), (2,-1)),
    ((0,1), (0,2), (-1,2)),
    ((1,0), (2,0), (2,1)),
    ((0,1), (0,2), (1,0)),
    ((0,1), (1,1), (2,1)),
    ((0,1), (1,0), (1,1)),
    ((0,1), (-1,1), (-1,2)),
    ((1,0), (1,1), (2,1)),
    ((0,1), (1,1), (1,2)),
    ((1,0), (1,-1), (2,-1)),
    ((0,1), (0,2), (-1,1)),
    ((0,1), (0,2), (1,1)),
    ((1,0), (2,0), (1,1)),
    ((1,0), (2,0), (1,-1)),
)

n,m = map(int,input().split())

a = [ list(map(int,input().split())) for _ in range(n) ]

ans = 0

for i in range(n):
    for j in range(m):
        for block in blocks:
            ok = True
            tmp = a[i][j]
            for dx,dy in block:
                x = j+dx
                y = i+dy
                if x<m and y <n:
                    tmp += a[y][x]
                else:
                    ok = False
            if ok and ans<tmp:
                ans=tmp
                
print(ans)
